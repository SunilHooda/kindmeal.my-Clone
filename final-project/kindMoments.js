function displayProduct(product, content, rows_per_page, page) {
    content.innerHTML = "";
    page--;
    let start = page * rows;
    let end = start + rows_per_page;

    let filtered = product.slice(start, end)
    filtered.map(function (ele) {
        let card = document.createElement("div");
        let box = document.createElement("div");
        box.style.cursor = "pointer";
        box.addEventListener("click", function () {
            window.location.href = "shop.html"
        })
        // div1
        div1 = document.createElement("div");

        let logo = document.createElement("img");
        logo.src = ele.logo;
        logo.style.cursor = "pointer";
        logo.addEventListener("click", function () {
            window.location.href = "profile.html"
        })


        let hbody = document.createElement("div");
        let name = document.createElement("p")
        name.innerText = ele.name;
        name.classList.add("name");
        name.addEventListener("click", function () {
            window.location.href = "profile.html"
        })
        let date = document.createElement("p")
        date.innerText = ele.date;
        date.classList.add("date")
        hbody.append(name, date)

        let btn = document.createElement("button");
        btn.innerText = "View"
        btn.classList.add("view")
        btn.addEventListener("click", function () {
            window.location.href = "profile.html"
        })
        div1.append(logo, hbody, btn)

        //div2
        // div2 = document.createElement("div");

        //sliding image
        let counter = 0;

        slideShow();
        function slideShow() {


            if (counter < ele.image.length) {
                let image = ele.image[counter];

                box.innerHTML = `<img src=${image}>`;
                counter++;
            } else {
                counter = 0;
            }
            setTimeout(slideShow, 2000);
        }

        //div3
        div3 = document.createElement("div");
        let likeImg = document.createElement("img");
        likeImg.src = ele.likeimg;
        let camara = document.createElement("img");
        camara.src = ele.camara
        let message = document.createElement("img");
        message.src = ele.msg;
        div3.append(likeImg, camara, message);
        //div4
        div4 = document.createElement("div");
        let para = document.createElement("p");

        para.innerText = ele.des;

        div4.append(para)

        //div5

        div5 = document.createElement("div");

        let socialimg = document.createElement("img");
        socialimg.src = ele.socialLogo;
        socialimg.style.cursor = "pointer";
        socialimg.addEventListener("click", function () {
            window.location.href = "https://www.facebook.com/"
        })

        //inner div
        divinner = document.createElement("div");
        let p1 = document.createElement("p");
        p1.style.cursor = "pointer";
        p1.classList.add("p1")
        p1.addEventListener("click", function () {
            window.location.href = "https://www.facebook.com/"
        })
        let p2 = document.createElement("p");
        p2.classList.add("date")

        if (ele.address !== undefined && ele.link !== undefined) {
            p1.innerText = ele.link;
            p2.innerText = ele.address
            divinner.append(p1, p2)
        }
        else if (ele.address !== undefined) {
            p1.innerText = ele.address;
            divinner.append(p1)
        }

        else if (ele.link !== undefined) {
            p2.innerText = ele.link
            divinner.append(p2)
        }



        if (ele.socialLogo !== undefined) {
            div5.append(socialimg, divinner);
        } else {
            div5.append(divinner);
        }

        card.append(div1, box, div3, div4, div5)
        document.querySelector("#content").append(card);
    })
}

//pagination 
function setupPagination(product, wrapper, rows_per_page) {
    wrapper.innerHTML = "";
    let page_count = Math.ceil(product.length / rows_per_page);
    for (let i = 1; i <= page_count; i++) {
        let btn = paginationButton(i, product);
        wrapper.appendChild(btn);
    }
}
function paginationButton(page, product) {
    let button = document.createElement("button");
    button.innerText = page;
    if (current_page == page) button.classList.add("active");
    button.addEventListener("click", function () {
        current_page = page;
        displayProduct(product, content, rows, current_page);
        let current_btn = document.querySelector(".pagenumbers button.active")
        current_btn.classList.remove("active");
        button.classList.add("active");
    })

    return button;
}
//pagination setup
setupPagination(product, pagination_element, rows);

